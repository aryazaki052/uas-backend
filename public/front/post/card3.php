    <h2>Things to do</h2>
    <p>
      This Nusa Penida Tour includes some activity such as visiting Broken
      beach, Kelingking beach, Angel Billabong beach, and Crystal Bay. This
      tour will start at 6.30 am from one of these following areas: Seminyak,
      Kuta, Jimbaran, Nusa Dua, or Legian. If you are not staying in one of
      those areas, please inform us of your location in details.
      <br />
      <br />
      You will be picked up on time at your lobby hotel. We will drive you to
      the harbor, and we will cross the sea by using a fast boat. After
      arriving in Nusa Penida Island, you will visit the Broken Beach. This
      beach can be said as a unique beach had by Nusa Penida. The seawater
      that is trapped in the cliffs makes a large pool. It is like a natural
      pool that has a unique view.
      <br />
      <br />
      The next destination to visit is Kelingking Beach. This is another
      famous beach in Nusa Penida. This beach is a hidden beach that has a
      unique view. The best place to take pictures is from the top of the
      cliff. The next beach to visit is Angel Billabong beach. This beach is
      located near the Broken beach. It takes approximately 10 minutes to get
      this beach. This destination is well known as the natural infinity pool,
      and you can do sunbathing on the coral.
      <br />
      <br />
      The last beach to visit is Crystal Bay beach. This beach will amaze you
      by its clear water, so you can see the underwater life clearly. This
      beach is the best place to do snorkeling or scuba diving. After all,
      activities are done, we will go back to Bali Island, and we will drive
      you back to your hotel.
    </p>
